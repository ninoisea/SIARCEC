    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id, Request $request)
    {
        $image = Image::findOrFail($id);
        $image->cheque()->detach($request->id);
        $image->delete();
        return response()->json($image);
    }
